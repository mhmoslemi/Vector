# Custom Dataset Wrapper for Unlearnable Examples
# ---------------------------------------------------------------------------
class PoisonedDataset(torch.utils.data.Dataset):
    def __init__(self, base_dataset, noise_path, perturb_type='samplewise'):
        self.base_dataset = base_dataset
        self.perturb_type = perturb_type
        
        print(f"Loading noise tensor from {noise_path}...")
        # Load the noise tensor generated by perturbation.py
        self.noise_tensor = torch.load(noise_path, map_location='cpu')
        
    def __len__(self):
        return len(self.base_dataset)

    def __getitem__(self, index):
        # Get the original image and label from the base dataset
        img, label = self.base_dataset[index]
        
        # Select the appropriate noise based on the generation method
        if self.perturb_type == 'samplewise':
            noise = self.noise_tensor[index]
        elif self.perturb_type == 'classwise':
            noise = self.noise_tensor[label]
        else:
            raise ValueError("perturb_type must be 'samplewise' or 'classwise'")
            
        # Ensure noise is a tensor and matches the image dtype/device
        if not isinstance(noise, torch.Tensor):
            noise = torch.tensor(noise)
            
        noise = noise.to(img.device).type(img.dtype)
        
        # Add the unlearnable noise to the image
        poisoned_img = img + noise
        
        # Note: Depending on your get_dataset() transforms, img might be in [0, 1] 
        # or normalized with mean/std. If it's simply ToTensor() [0, 1], you may 
        # want to clamp it to ensure valid pixel values:
        # poisoned_img = torch.clamp(poisoned_img, 0.0, 1.0)
        
        return poisoned_img, label


