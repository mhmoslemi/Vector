class _DenseLayer(nn.Module):
    def __init__(self, in_channels, growth_rate, bn_size=4):
        super().__init__()
        mid = bn_size * growth_rate
        self.block = nn.Sequential(
            nn.BatchNorm2d(in_channels), nn.ReLU(inplace=True),
            nn.Conv2d(in_channels, mid, kernel_size=1, bias=False),
            nn.BatchNorm2d(mid), nn.ReLU(inplace=True),
            nn.Conv2d(mid, growth_rate, kernel_size=3, padding=1, bias=False),
        )

    def forward(self, x):
        return torch.cat([x, self.block(x)], dim=1)


class _DenseBlock(nn.Sequential):
    def __init__(self, num_layers, in_channels, growth_rate):
        super().__init__()
        for i in range(num_layers):
            self.add_module(f'layer{i}', _DenseLayer(in_channels + i * growth_rate, growth_rate))


class _Transition(nn.Sequential):
    def __init__(self, in_channels, out_channels):
        super().__init__(
            nn.BatchNorm2d(in_channels), nn.ReLU(inplace=True),
            nn.Conv2d(in_channels, out_channels, kernel_size=1, bias=False),
            nn.AvgPool2d(kernel_size=2, stride=2),
        )


class DenseNet121(nn.Module):
    def __init__(self, num_classes=10, in_channels=3,
                 growth_rate=32, block_config=(6, 12, 24, 16), init_features=64):
        super().__init__()
        self.features = nn.Sequential(
            nn.Conv2d(in_channels, init_features, kernel_size=7, stride=2, padding=3, bias=False),
            nn.BatchNorm2d(init_features), nn.ReLU(inplace=True),
            nn.MaxPool2d(kernel_size=3, stride=2, padding=1),
        )
        c = init_features
        for i, num_layers in enumerate(block_config):
            self.features.add_module(f'denseblock{i+1}', _DenseBlock(num_layers, c, growth_rate))
            c += num_layers * growth_rate
            if i < len(block_config) - 1:
                out = c // 2
                self.features.add_module(f'transition{i+1}', _Transition(c, out))
                c = out
        self.features.add_module('norm_final', nn.BatchNorm2d(c))
        self.classifier = nn.Linear(c, num_classes)

    def forward(self, x):
        x = self.features(x)
        x = F.relu(x, inplace=True)
        x = F.adaptive_avg_pool2d(x, 1).flatten(1)
        return self.classifier(x)


