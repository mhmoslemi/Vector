class _PatchEmbed(nn.Module):
    def __init__(self, in_channels, patch_size, embed_dim):
        super().__init__()
        self.proj = nn.Conv2d(in_channels, embed_dim, kernel_size=patch_size, stride=patch_size)

    def forward(self, x):
        return self.proj(x).flatten(2).transpose(1, 2)


class _TransformerBlock(nn.Module):
    def __init__(self, embed_dim, num_heads, mlp_ratio=4.0, dropout=0.0):
        super().__init__()
        self.norm1 = nn.LayerNorm(embed_dim)
        self.attn  = nn.MultiheadAttention(embed_dim, num_heads, dropout=dropout, batch_first=True)
        self.norm2 = nn.LayerNorm(embed_dim)
        mlp_dim    = int(embed_dim * mlp_ratio)
        self.mlp   = nn.Sequential(
            nn.Linear(embed_dim, mlp_dim), nn.GELU(),
            nn.Dropout(dropout),
            nn.Linear(mlp_dim, embed_dim), nn.Dropout(dropout),
        )

    def forward(self, x):
        h = self.norm1(x)
        x = x + self.attn(h, h, h, need_weights=False)[0]
        return x + self.mlp(self.norm2(x))


class ViT(nn.Module):
    """ViT-B/16 config: embed_dim=768, depth=12, heads=12. patch_size=4 for <=64px images."""
    def __init__(self, num_classes=10, in_channels=3, im_size=(32, 32),
                 embed_dim=768, depth=12, num_heads=12, mlp_ratio=4.0, dropout=0.0):
        super().__init__()
        patch_size = 4 if im_size[0] <= 64 else 16
        num_patches = (im_size[0] // patch_size) * (im_size[1] // patch_size)

        self.patch_embed = _PatchEmbed(in_channels, patch_size, embed_dim)
        self.cls_token   = nn.Parameter(torch.zeros(1, 1, embed_dim))
        self.pos_embed   = nn.Parameter(torch.zeros(1, num_patches + 1, embed_dim))
        nn.init.trunc_normal_(self.cls_token, std=0.02)
        nn.init.trunc_normal_(self.pos_embed, std=0.02)

        self.blocks = nn.Sequential(*[
            _TransformerBlock(embed_dim, num_heads, mlp_ratio, dropout)
            for _ in range(depth)
        ])
        self.norm = nn.LayerNorm(embed_dim)
        self.head = nn.Linear(embed_dim, num_classes)

    def forward(self, x):
        B = x.size(0)
        x = self.patch_embed(x)
        cls = self.cls_token.expand(B, -1, -1)
        x   = torch.cat([cls, x], dim=1) + self.pos_embed
        x   = self.blocks(x)
        return self.head(self.norm(x[:, 0]))


def build_emn_model(name, num_classes, channel, im_size=(32, 32)):
    """Instantiate a model by name. Supports EMN ResNets and DD-DC ConvNet."""
    if name == 'ConvNet':
        from networks import ConvNet as _ConvNet
        return _ConvNet(channel=channel, num_classes=num_classes,
                        net_width=128, net_depth=3, net_act='relu',
                        net_norm='instancenorm', net_pooling='avgpooling',
                        im_size=im_size)

    if name in ('ResNet18', 'resnet18'):
        return ResNet18(num_classes, channel)
    if name in ('ResNet20', 'resnet20'):
        return ResNet20(num_classes, channel)
    if name in ('ResNet32', 'resnet32'):
        return ResNet32(num_classes, channel)
    if name in ('ResNet50', 'resnet50'):
        return ResNet50(num_classes, channel)

    if name in ('VGG11', 'vgg11'):
        return VGG11(num_classes=num_classes, in_channels=channel)

    if name in ('DenseNet121', 'densenet121'):
        return DenseNet121(num_classes=num_classes, in_channels=channel)

    if name in ('ViT', 'vit'):
        return ViT(num_classes=num_classes, in_channels=channel, im_size=im_size)

    raise ValueError(f"Unknown model: {name}")

